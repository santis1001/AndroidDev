package com.example.intracer.Fr_groups;

public class UserList {

    String email;
    String Datesc;

    public UserList(String email, String datesc) {
        this.email = email;
        Datesc = datesc;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDatesc() {
        return Datesc;
    }

    public void setDatesc(String datesc) {
        Datesc = datesc;
    }



}
